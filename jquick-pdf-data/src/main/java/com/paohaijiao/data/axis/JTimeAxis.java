package com.paohaijiao.data.axis;

import com.paohaijiao.data.code.JAxisType;

/**
 * 时间型坐标轴用法同数值型，只是目标处理和格式化显示时会自动转变为时间，并且随着时间跨度的不同自动切换需要显示的时间粒度
 *
 * @author martin
 */
public class JTimeAxis extends JValueAxis {
    /**
     * 构造函数
     */
    public JTimeAxis() {
        this.type(JAxisType.time);
    }
}
